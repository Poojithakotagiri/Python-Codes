weekday = int(input("enter your number(1-7):"))
if weekday == 1:
    print("\nmonday");

elif weekday == 2:
    print("\ntuesday")

elif weekday == 3:
    print("\nwed")

elif weekday == 4:
    print("\nthursday")

elif weekday == 5:
    print("\nfriday")

elif weekday == 6:
    print("\nsaturday")

elif weekday == 7:
    print("\nsunday")
else :
    print("\nplease enter weekday number 1-7.")
